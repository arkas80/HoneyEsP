#ifndef SENSORS_H
// Εξήγηση: "ΕΑΝ ΔΕΝ έχει οριστεί το SENSORS_H" - Αποτρέπει την διπλή συμπερίληψη του αρχείου.
// Αν το αρχείο έχει ήδη συμπεριληφθεί μια φορά, αυτή η γραμμή το εμποδίζει να ξαναμπεί.

#define SENSORS_H
// Εξήγηση: ΟΡΙΖΕΙ το SENSORS_H - Σημαδεύει το αρχείο ως "ήδη συμπεριληφθέν".
// Την επόμενη φορά που θα γίνει #include, το #ifndef θα είναι false και θα παρακάμψει όλο το περιεχόμενο.

#include <Arduino.h>
// Εξήγηση: ΣΥΜΠΕΡΙΛΑΜΒΑΝΕΙ την βασική βιβλιοθήκη του Arduino.
// Δίνει πρόσβαση σε: String, float, int, byte, millis(), delay(), Serial, κλπ.

#include <HX711.h>
// Εξήγηση: ΣΥΜΠΕΡΙΛΑΜΒΑΝΕΙ την βιβλιοθήκη για την ζυγαριά HX711.
// Περιέχει κλάσεις και συναρτήσεις για ανάγνωση βάρους, βαθμονόμηση, κλπ.

// ============================================================================================
// EXTERNAL OBJECTS (για πρόσβαση από άλλα αρχεία)
// ============================================================================================
// Εξήγηση: Διαχωριστικό σχόλιο - Δηλώνει ότι ακολουθούν αντικείμενα που είναι ορατά σε ΟΛΟ το project.

extern HX711 scale;
// Εξήγηση: EXTERN = "υπάρχει εξωτερικά/αλλού" - ΔΕΝ δημιουργεί νέο αντικείμενο.
// Απλά λέει στον compiler: "Πιστεψέ με, υπάρχει ένα HX711 που λέγεται scale σε κάποιο άλλο .cpp αρχείο".
// Χωρίς το extern, θα δημιουργούταν ΔΕΥΤΕΡΟ αντικείμενο και θα είχαμε διπλό ορισμό (link error).

// Status flags για να ξέρουμε ποιοι αισθητήρες δουλεύουν
// Εξήγηση: Σχόλιο - Περιγράφει την επόμενη ομάδα μεταβλητών.

extern bool bmeOK;
// Εξήγηση: EXTERN bool - Δηλώνει ότι υπάρχει μια μεταβλητή bmeOK αλλού.
// true = ο BME280 αισθητήρας βρέθηκε και λειτουργεί, false = δεν βρέθηκε.

extern bool dsOK;
// Εξήγηση: EXTERN bool - Δηλώνει ότι υπάρχει μεταβλητή dsOK αλλού.
// true = ο DS18B20 αισθητήρας βρέθηκε, false = δεν βρέθηκε.

extern bool hx711OK;
// Εξήγηση: EXTERN bool - Δηλώνει ότι υπάρχει μεταβλητή hx711OK αλλού.
// true = η ζυγαριά HX711 βρέθηκε και είναι έτοιμη, false = πρόβλημα.

extern bool hasMAX17048;
// Εξήγηση: EXTERN bool - Δηλώνει ότι υπάρχει μεταβλητή hasMAX17048 αλλού.
// true = το chip MAX17048 (μπαταρία) βρέθηκε στο I2C, false = δεν υπάρχει, θα χρησιμοποιηθεί το AXP2101.

// ============================================================================================
// INITIALIZATION
// ============================================================================================
// Εξήγηση: Διαχωριστικό - Ενότητα αρχικοποίησης.

void initSensors();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ (χωρίς σώμα) - ΠΡΩΤΟΤΥΠΟ.
// Λέει στον compiler: "Υπάρχει μια συνάρτηση που λέγεται initSensors, δεν επιστρέφει τίποτα (void),
// και δεν παίρνει παραμέτρους ()". Το σώμα { ... } βρίσκεται στο Sensors.cpp.
// ΚΑΛΕΙΤΑΙ: ΜΙΑ ΦΟΡΑ στο setup() για να αρχικοποιήσει ΟΛΟΥΣ τους αισθητήρες.

// ============================================================================================
// WEIGHT (HX711)
// ============================================================================================
// Εξήγηση: Διαχωριστικό - Ενότητα βάρους.

float getWeight();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει float (δεκαδικό αριθμό).
// Επιστρέφει το βάρος σε ΓΡΑΜΜΑΡΙΑ (grams).
// Παράδειγμα: 2450.5 = 2.45 κιλά.
// ΚΑΛΕΙΤΑΙ: Κάθε φορά που θέλουμε να διαβάσουμε το βάρος.

// ============================================================================================
// ENVIRONMENT (BME280)
// ============================================================================================
// Εξήγηση: Διαχωριστικό - Ενότητα περιβαλλοντικών μετρήσεων.

void getBME(float &t, float &p, float &h);
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - ΔΕΝ επιστρέφει τιμή (void), αλλά γεμίζει τις τρεις παραμέτρους.
// &t = "pass by reference" - Η συνάρτηση ΜΠΟΡΕΙ να αλλάξει την τιμή του t έξω.
// t = θερμοκρασία αέρα σε °C (Celsius)
// p = ατμοσφαιρική πίεση σε hPa (εκατοστά Pascal)
// h = σχετική υγρασία σε % (percent)
// ΚΑΛΕΙΤΑΙ: Κάθε φορά που θέλουμε μετρήσεις περιβάλλοντος.

// ============================================================================================
// HIVE TEMPERATURE (DS18B20)
// ============================================================================================
// Εξήγηση: Διαχωριστικό - Ενότητα θερμοκρασίας κυψέλης.

float getDS();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει float.
// Επιστρέφει την θερμοκρασία ΜΕΣΑ στην κυψέλη σε °C.
// ΚΑΛΕΙΤΑΙ: Κάθε φορά που θέλουμε την εσωτερική θερμοκρασία.

// ============================================================================================
// BATTERY (Υποστηρίζει AXP2101 & MAX17048 αυτόματα)
// ============================================================================================
// Εξήγηση: Διαχωριστικό - Ενότητα μπαταρίας. ΥΠΟΣΤΗΡΙΖΕΙ ΚΑΙ ΤΑ ΔΥΟ CHIP ΑΥΤΟΜΑΤΑ.

float getBatteryVoltage();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει float.
// Επιστρέφει την ΤΑΣΗ της μπαταρίας σε VOLTS (3.0V - 4.2V).
// Αν δεν υπάρχει μπαταρία, επιστρέφει 4.2V για να δείχνει 100%.
// Υποστηρίζει ΑΥΤΟΜΑΤΑ: MAX17048 (προτεραιότητα) ή AXP2101 (fallback).
// ΚΑΛΕΙΤΑΙ: Για debug ή αν θέλουμε την τάση αντί για ποσοστό.

float getBatteryPercent();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει float.
// Επιστρέφει το ΠΟΣΟΣΤΟ της μπαταρίας (0% - 100%).
// Αν δεν υπάρχει μπαταρία, επιστρέφει 100% (USB power).
// Υποστηρίζει ΑΥΤΟΜΑΤΑ: MAX17048 (άμεσο ποσοστό) ή AXP2101 (υπολογισμό από τάση).
// ΚΑΛΕΙΤΑΙ: Για εμφάνιση στην οθόνη και για έλεγχο low battery.

void printBatteryDebug();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - ΔΕΝ επιστρέφει τιμή (void).
// Εκτυπώνει στο Serial ΟΛΕΣ τις πληροφορίες μπαταρίας για debugging:
// - MAX17048 present? (NAI/OXI)
// - USB Voltage (τάση USB)
// - Battery present? (NAI/OXI)
// - Final Voltage (τελική τάση)
// - Final Percent (τελικό ποσοστό)
// - AXP2101 Raw (ακατέργαστη τιμή)
// ΚΑΛΕΙΤΑΙ: Προαιρετικά, για debug στο setup().

bool isBatteryPresent();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει bool (true/false).
// Ελέγχει ΑΥΤΟΜΑΤΑ αν υπάρχει μπαταρία ή αν τροφοδοτούμαστε μόνο από USB.
// ΑΛΓΟΡΙΘΜΟΣ:
//   1. Αν υπάρχει MAX17048 → return true (σίγουρα μπαταρία)
//   2. Αν ΔΕΝ υπάρχει USB → return true (πρέπει να υπάρχει μπαταρία)
//   3. Αν USB connected:
//      - Αν τάση 4.2V σταθερή → return false (USB only, no battery)
//      - Αν τάση 3.7V σταθερή → return false (USB only, no battery)
//      - Αλλιώς → return true (υπάρχει μπαταρία)
// ΚΑΛΕΙΤΑΙ: Αυτόματα από getBatteryVoltage() και getBatteryPercent().

// ============================================================================================
// SCALE CALIBRATION
// ============================================================================================
// Εξήγηση: Διαχωριστικό - Ενότητα βαθμονόμησης ζυγαριάς.

void tareScale();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - ΔΕΝ επιστρέφει τιμή.
// ΜΗΔΕΝΙΖΕΙ την ζυγαριά με την ΚΥΨΕΛΗ πάνω.
// Διαβάζει 40 μετρήσεις, υπολογίζει τον μέσο όρο και τον αποθηκεύει ως νέο μηδέν.
// ΠΡΟΣΟΧΗ: Πρέπει να έχει ΑΦΑΙΡΕΘΕΙ κάθε βάρος από την κυψέλη!
// ΚΑΛΕΙΤΑΙ: Μέσα από web interface ή όταν χρειάζεται επαναμηδενισμός.

void setCalibration(float factor);
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - ΥΠΕΡΦΟΡΤΩΣΗ (overload).
// Ορίζει τον ΣΥΝΤΕΛΕΣΤΗ ΒΑΘΜΟΝΟΜΗΣΗΣ (scale factor) ΧΩΡΙΣ αναστροφή (invert = false).
// factor: πόσα γραμμάρια αντιστοιχούν σε 1 μονάδα από τον HX711.
// ΚΑΛΕΙΤΑΙ: Μέσα από web interface.

void setCalibration(float factor, bool invert);
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - ΥΠΕΡΦΟΡΤΩΣΗ (overload).
// Ορίζει τον ΣΥΝΤΕΛΕΣΤΗ ΒΑΘΜΟΝΟΜΗΣΗΣ (scale factor) ΜΕ επιλογή αναστροφής.
// factor: πόσα γραμμάρια αντιστοιχούν σε 1 μονάδα.
// invert: true = αναστρέφει το σήμα (αν η ζυγαριά δείχνει αρνητικά).
// ΚΑΛΕΙΤΑΙ: Μέσα από web interface.

long getScaleOffset();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει long (ακέραιος 32-bit).
// Επιστρέφει την τιμή OFFSET (μηδέν) της ζυγαριάς.
// Χρήσιμο για debug και αποθήκευση.
// ΚΑΛΕΙΤΑΙ: Για επαλήθευση αποθηκευμένων ρυθμίσεων.

float getScaleFactor();
// Εξήγηση: ΔΗΛΩΣΗ ΣΥΝΑΡΤΗΣΗΣ - Επιστρέφει float.
// Επιστρέφει τον ΣΥΝΤΕΛΕΣΤΗ ΒΑΘΜΟΝΟΜΗΣΗΣ (scale factor).
// Χρήσιμο για debug και αποθήκευση.
// ΚΑΛΕΙΤΑΙ: Για επαλήθευση αποθηκευμένων ρυθμίσεων.

#endif
